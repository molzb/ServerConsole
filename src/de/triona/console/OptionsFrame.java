    private void btnSave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave1ActionPerformed
        if (txtPkg.getText().isEmpty()) {
            String msg = "Do you really want to leave the package name empty?\n";
            msg += "There will be no highlighting of your package in the console";
            int res = JOptionPane.showConfirmDialog(this, msg, "WARNING", WARNING_MESSAGE);
            if (res == NO_OPTION || res == CANCEL_OPTION) {
                return;
            }
        }
        ConsoleFrame.OWN_PKG = txtPkg.getText();
        dispose();
    }//GEN-LAST:event_btnSave1ActionPerformed


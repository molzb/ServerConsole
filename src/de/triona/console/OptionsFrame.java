    public boolean validateServerSettings(String serverDir, String startCmd, String stopCmd, String ownPkg) {
        // Check if the server directory exists
        String errorTitle = "ERROR in Server Settings";
        if (!new File(serverDir).exists()) {
            String msg = serverDir + " doesn't exist. It should be sth. like C:/jboss7/";
            JOptionPane.showMessageDialog(this, msg, errorTitle, ERROR_MESSAGE);
            return false;
        }
        // Check Server start command
        String startCommand = serverDir + startCmd;
        if (startCommand.contains(" ")) {
            startCommand = startCommand.substring(0, startCommand.indexOf(' '));
            logger.info("StartCommand without extension='" + startCommand + "'");
        }
        if (!new File(startCommand).exists()) {
            String msg = startCmd + " doesn't exist. It should be sth. like standalone.xml";
            JOptionPane.showMessageDialog(this, msg, errorTitle, ERROR_MESSAGE);
            return false;
        }
        // Check Server stop command
        String stopCommand = serverDir + stopCmd;
        if (stopCommand.contains(" ")) {
            stopCommand = stopCommand.substring(0, stopCommand.indexOf(' '));
            logger.info("StopCommand without extension='" + stopCommand + "'");
        }
        if (!new File(stopCommand).exists()) {
            String msg = stopCmd + " doesn't exist. It should be sth. like jboss-cli.bat";
            JOptionPane.showMessageDialog(this, msg, errorTitle, ERROR_MESSAGE);
            return false;
        }
        // Check package name
        String regex = "[a-z\\.]{1,}";
        if (!Pattern.matches(regex, ownPkg)) {
            String msg = ownPkg + " is not a valid package name. It should be sth. de.mycompany.proj";
            JOptionPane.showMessageDialog(this, msg, errorTitle, ERROR_MESSAGE);
            return false;
        }

        return true;
    }

